// gen-builtins reads a JSON builtin catalog (produced by extract-builtins)
// and emits a Go source file containing builtinsregistry.Register() calls
// for each function. The generated code registers metadata-only stubs with
// no Fn closures, enabling function-name validation and overload resolution
// without a live database.
//
// Usage:
//
//	go run ./cmd/gen-builtins \
//	  -input  internal/builtinstubs/testdata/crdb_builtins_v26.2.json \
//	  -output internal/builtinstubs/stubs_v26_2_gen.go \
//	  -version v26.2
package main

import (
	"encoding/json"
	"flag"
	"fmt"
	"log"
	"os"
	"sort"
	"strings"
	"text/template"
)

type catalog struct {
	Version   string     `json:"version"`
	Functions []function `json:"functions"`
}

type function struct {
	Name       string     `json:"name"`
	Properties properties `json:"properties"`
	Overloads  []overload `json:"overloads"`
}

type properties struct {
	Category                string `json:"category,omitempty"`
	AvailableOnPublicSchema bool   `json:"available_on_public_schema,omitempty"`
	Undocumented            bool   `json:"undocumented,omitempty"`
}

type overload struct {
	Params         []param `json:"params,omitempty"`
	ParamKind      string  `json:"param_kind"`
	VariadicType   *typ    `json:"variadic_type,omitempty"`
	ReturnType     *typ    `json:"return_type,omitempty"`
	ReturnTypeKind string  `json:"return_type_kind"`
	Volatility     string  `json:"volatility"`
	Class          string  `json:"class"`
	CalledOnNull   bool    `json:"called_on_null_input,omitempty"`
	Preference     int     `json:"preference,omitempty"`
}

type param struct {
	Name string `json:"name,omitempty"`
	Type typ    `json:"type"`
}

type typ struct {
	OID    uint32 `json:"oid"`
	Name   string `json:"name"`
	Family string `json:"family,omitempty"`
}

