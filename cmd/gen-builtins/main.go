func typeExpr(t typ) string {
	if expr, ok := oidToGoExpr[t.OID]; ok {
		return expr
	}
	// For array types, use types.MakeArray with the element type.
	// Array OIDs in postgres are generally the element OID's array variant.
	// We use OidToType as the runtime fallback.
	return fmt.Sprintf("oidTyp(%d)", t.OID)
}

func volatilityExpr(v string) string {
	switch v {
	case "immutable":
		return "volatility.Immutable"
	case "stable":
		return "volatility.Stable"
	case "volatile":
		return "volatility.Volatile"
	default:
		return "volatility.Volatile"
	}
}

func classExpr(c string) string {
	switch c {
	case "normal":
		return "tree.NormalClass"
	case "aggregate":
		return "tree.AggregateClass"
	case "window":
		return "tree.WindowClass"
	case "generator":
		return "tree.GeneratorClass"
	default:
		return "tree.NormalClass"
	}
}

func versionToIdent(v string) string {
	r := strings.NewReplacer(".", "_", "-", "_")
	return r.Replace(strings.TrimPrefix(v, "v"))
}

func goStringLit(s string) string {
	b, _ := json.Marshal(s)
	return string(b)
}

var funcMap = template.FuncMap{
	"typeExpr":       typeExpr,
	"volatilityExpr": volatilityExpr,
	"classExpr":      classExpr,
	"goStringLit":    goStringLit,
}

var tmpl = template.Must(template.New("stubs").Funcs(funcMap).Parse(`// Code generated by gen-builtins from {{.InputFile}}; DO NOT EDIT.

package builtinstubs

import (
	"github.com/cockroachdb/cockroachdb-parser/pkg/sql/sem/builtins/builtinsregistry"
	"github.com/cockroachdb/cockroachdb-parser/pkg/sql/sem/tree"
	"github.com/cockroachdb/cockroachdb-parser/pkg/sql/sem/volatility"
	"github.com/cockroachdb/cockroachdb-parser/pkg/sql/types"
)

// oidTyp is shared across versions; see internal/builtinstubs/typehelpers.go.

func registerV{{.VersionIdent}}() {
{{- range .Functions}}
	builtinsregistry.Register({{goStringLit .Name}}, &tree.FunctionProperties{
{{- if .Properties.Category}}
		Category: {{goStringLit .Properties.Category}},
{{- end}}
{{- if .Properties.AvailableOnPublicSchema}}
		AvailableOnPublicSchema: true,
{{- end}}
{{- if .Properties.Undocumented}}
		Undocumented: true,
{{- end}}
	}, []tree.Overload{
{{- range .Overloads}}
		{
{{- if eq .ParamKind "fixed"}}
{{- if .Params}}
			Types: tree.ParamTypes{
{{- range .Params}}
				{Name: {{goStringLit .Name}}, Typ: {{typeExpr .Type}}},
{{- end}}
			},
{{- else}}
			Types: tree.ParamTypes{},
{{- end}}
{{- else if eq .ParamKind "homogeneous"}}
			Types: tree.HomogeneousType{},
{{- else if eq .ParamKind "variadic"}}
			Types: tree.VariadicType{
{{- if .Params}}
				FixedTypes: []*types.T{
{{- range .Params}}
					{{typeExpr .Type}},
{{- end}}
				},
{{- end}}
				VarType: {{typeExpr .VariadicType}},
			},
{{- end}}
			ReturnType:       tree.FixedReturnType({{typeExpr .ReturnType}}),
			Volatility:       {{volatilityExpr .Volatility}},
			Class:            {{classExpr .Class}},
{{- if .CalledOnNull}}
			CalledOnNullInput: true,
{{- end}}
{{- if ne .Preference 0}}
			OverloadPreference: tree.OverloadPreference({{.Preference}}),
{{- end}}
		},
{{- end}}
	})
{{end -}}
}
`))

type templateData struct {
	InputFile    string
	VersionIdent string
	Functions    []function
}

func main() {
	inputFile := flag.String("input", "", "path to JSON catalog file")
	outputFile := flag.String("output", "", "path to output Go file")
	version := flag.String("version", "", "CRDB version (e.g., v26.2)")
	flag.Parse()

	if *inputFile == "" || *outputFile == "" || *version == "" {
		log.Fatal("usage: gen-builtins -input FILE -output FILE -version VERSION")
	}

	data, err := os.ReadFile(*inputFile)
	if err != nil {
		log.Fatalf("reading input: %v", err)
	}

	var cat catalog
	if err := json.Unmarshal(data, &cat); err != nil {
		log.Fatalf("parsing JSON: %v", err)
	}

	sort.Slice(cat.Functions, func(i, j int) bool {
		return cat.Functions[i].Name < cat.Functions[j].Name
	})

	// Filter out functions with no overloads.
	var filtered []function
	for _, f := range cat.Functions {
		if len(f.Overloads) > 0 {
			filtered = append(filtered, f)
		}
	}

	td := templateData{
		InputFile:    *inputFile,
		VersionIdent: versionToIdent(*version),
		Functions:    filtered,
	}

	out, err := os.Create(*outputFile)
	if err != nil {
		log.Fatalf("creating output: %v", err)
	}

	if err := tmpl.Execute(out, td); err != nil {
		_ = out.Close()
		log.Fatalf("executing template: %v", err)
	}
	if err := out.Close(); err != nil {
		log.Fatalf("closing output: %v", err)
	}

	fmt.Fprintf(os.Stderr, "generated %s: %d functions, %d overloads\n",
		*outputFile, len(td.Functions), countOverloads(td.Functions))
}

func countOverloads(funcs []function) int {
	n := 0
	for _, f := range funcs {
		n += len(f.Overloads)
	}
	return n
}
